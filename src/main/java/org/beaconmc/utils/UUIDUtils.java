 package org.beaconmc.utils;

import java.util.UUID;

public class UUIDUtils {

    public static UUID fromMojangString(String uuid){
        return UUID.fromString(uuid.replaceAll("(\\w{8})(\\w{4})(\\w{4})(\\w{4})(\\w{12})", "$1-$2-$3-$4-$5"));
    }

}
